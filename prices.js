export const findCraftSum = (elem, buyPrices, sellPrices) => {
  let totalPrice = 0;
  for (let i = 0; i < elem.getAttribute("data-ingredient-count"); i++) {
    const ingredientID = elem.getAttribute(`data-ingredient-${i}-id`);
    const ingredientCount = elem.getAttribute(`data-ingredient-${i}-count`);
    const ingredientPrice = buyPrices[ingredientID] || sellPrices[ingredientID];
    if (ingredientPrice) {
      totalPrice += ingredientPrice * ingredientCount;
    }
  }
  return totalPrice;
};
